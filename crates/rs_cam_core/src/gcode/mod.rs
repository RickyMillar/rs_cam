/// A phase in a multi-operation job: toolpath + spindle speed + label.
pub struct GcodePhase<'a> {
    pub toolpath: &'a Toolpath,
    pub spindle_rpm: u32,
    pub label: &'a str,
    /// Raw G-code to emit before this toolpath's moves (user-defined).
    pub pre_gcode: Option<&'a str>,
    /// Raw G-code to emit after this toolpath's moves (user-defined).
    pub post_gcode: Option<&'a str>,
    /// Tool number for M6 tool change. If `Some` and different from previous phase,
    /// an M6 T{n} command is emitted before this phase.
    pub tool_number: Option<u32>,
    /// Coolant mode for this phase. Coolant on/off commands are emitted
    /// when the mode changes between phases.
    pub coolant: CoolantMode,
    /// Controller cutter compensation (G41/G42). When `Some`, a G41/G42
    /// command is emitted before the first cutting move and G40 after the last.
    pub controller_compensation: Option<ControllerCompensation>,
}

/// Emit G-code from multiple phases, inserting tool changes, spindle speed
/// changes, and coolant commands between operations as needed.
pub(crate) fn emit_gcode_phased(phases: &[GcodePhase<'_>], post: &dyn PostProcessor) -> String {
    if phases.is_empty() {
        return String::new();
    }

    let mut output = String::new();
    // SAFETY: early return above guarantees phases is non-empty
    #[allow(clippy::indexing_slicing)]
    let first_phase = &phases[0];
    let first_rpm = first_phase.spindle_rpm;
    output.push_str(&post.preamble(first_rpm));

    // Emit coolant start if the first phase has coolant enabled
    let first_coolant = first_phase.coolant;
    if first_coolant.is_active() {
        output.push_str(first_coolant.start_gcode());
    }

    let mut current_rpm = first_rpm;
    let mut current_tool: Option<u32> = first_phase.tool_number;
    let mut current_coolant = first_coolant;
    let mut last_feed: Option<f64> = None;

    for (idx, phase) in phases.iter().enumerate() {
        output.push_str(&post.comment(phase.label));

        // Tool change: emit M6 T{n} if tool number changed
        if idx > 0
            && let Some(tool_num) = phase.tool_number
            && current_tool != Some(tool_num)
        {
            // Stop spindle and coolant before tool change
            if current_coolant.is_active() {
                let _ = writeln!(output, "M9");
            }
            let _ = writeln!(output, "M5");
            let _ = writeln!(output, "M6 T{tool_num}");
            // Restart spindle after tool change
            let _ = writeln!(output, "M3 S{}", phase.spindle_rpm);
            current_rpm = phase.spindle_rpm;
            current_tool = Some(tool_num);
            // Restart coolant after tool change if needed
            if phase.coolant.is_active() {
                output.push_str(phase.coolant.start_gcode());
            }
            current_coolant = phase.coolant;
            last_feed = None;
        }

        // Spindle speed change (only if we didn't already emit it in the tool change block)
        if phase.spindle_rpm != current_rpm {
            let _ = writeln!(output, "M3 S{}", phase.spindle_rpm);
            current_rpm = phase.spindle_rpm;
        }

        // Coolant mode change (only if we didn't already handle it in tool change)
        if idx > 0
            && phase.coolant != current_coolant
            && !(phase.tool_number.is_some() && current_tool == phase.tool_number)
        {
            if current_coolant.is_active() && !phase.coolant.is_active() {
                let _ = writeln!(output, "M9");
            } else if phase.coolant.is_active() {
                if current_coolant.is_active() {
                    let _ = writeln!(output, "M9");
                }
                output.push_str(phase.coolant.start_gcode());
            }
            current_coolant = phase.coolant;
        }

        if let Some(pre) = phase.pre_gcode
            && !pre.is_empty()
        {
            output.push_str(pre);
            if !pre.ends_with('\n') {
                output.push('\n');
            }
        }

        // Controller compensation: emit G41/G42 before first cutting move
        let comp = phase.controller_compensation;
        let mut comp_started = false;
        let tool_num_for_comp = phase.tool_number.unwrap_or(1);

        for m in &phase.toolpath.moves {
            match m.move_type {
                MoveType::Rapid => {
                    output.push_str(&post.rapid(m.target.x, m.target.y, m.target.z));
                    last_feed = None;
                }
                MoveType::Linear { feed_rate } => {
                    if let Some(dir) = comp
                        && !comp_started
                    {
                        let code = match dir {
                            ControllerCompensation::Left => "G41",
                            ControllerCompensation::Right => "G42",
                        };
                        let _ = writeln!(output, "{code} D{tool_num_for_comp}");
                        comp_started = true;
                    }
                    if last_feed != Some(feed_rate) {
                        output
                            .push_str(&post.linear(m.target.x, m.target.y, m.target.z, feed_rate));
                        last_feed = Some(feed_rate);
                    } else {
                        let dp = post.decimal_places();
                        let _ = writeln!(
                            output,
                            "G1 X{:.dp$} Y{:.dp$} Z{:.dp$}",
                            m.target.x, m.target.y, m.target.z
                        );
                    }
                }
                MoveType::ArcCW { i, j, feed_rate } => {
                    if let Some(dir) = comp
                        && !comp_started
                    {
                        let code = match dir {
                            ControllerCompensation::Left => "G41",
                            ControllerCompensation::Right => "G42",
                        };
                        let _ = writeln!(output, "{code} D{tool_num_for_comp}");
                        comp_started = true;
                    }
                    output.push_str(
                        &post.arc_cw(m.target.x, m.target.y, m.target.z, i, j, feed_rate),
                    );
                    last_feed = Some(feed_rate);
                }
                MoveType::ArcCCW { i, j, feed_rate } => {
                    if let Some(dir) = comp
                        && !comp_started
                    {
                        let code = match dir {
                            ControllerCompensation::Left => "G41",
                            ControllerCompensation::Right => "G42",
                        };
                        let _ = writeln!(output, "{code} D{tool_num_for_comp}");
                        comp_started = true;
                    }
                    output.push_str(
                        &post.arc_ccw(m.target.x, m.target.y, m.target.z, i, j, feed_rate),
                    );
                    last_feed = Some(feed_rate);
                }
            }
        }

        // Cancel controller compensation after the last cutting move
        if comp_started {
            let _ = writeln!(output, "G40");
        }

        if let Some(post_gc) = phase.post_gcode
            && !post_gc.is_empty()
        {
            output.push_str(post_gc);
            if !post_gc.ends_with('\n') {
                output.push('\n');
            }
        }
    }

    // Turn off coolant before postamble if it was active
    if current_coolant.is_active() {
        let _ = writeln!(output, "M9");
    }

    output.push_str(&post.postamble());
    output
}

/// Emit checked G-code from a project session.
pub fn export_gcode_checked(
    project: &ProjectSession,
    sim_trace: Option<&SimulationCutTrace>,
    policy: ToolLoadExportPolicy,
) -> Result<String, ExportError> {
    // Run the project-level tool-load report and gate on policy. Phase 1a
    // ships the deflection criterion (purely geometric L/D); chipload and
    // power are stubs returning `Unmodeled(NotImplemented)` and are therefore
    // gated behind `policy.accept_unmodeled`.
    let report = project_load_report(project, sim_trace);
    enforce_load_policy(&report, &policy)?;

    let post_format = match project.post_config().format.to_ascii_lowercase().as_str() {
        "linuxcnc" | "linux_cnc" => PostFormat::LinuxCnc,
        "mach3" => PostFormat::Mach3,
        _ => PostFormat::Grbl,
    };
    let post = post_format.post_processor();

    let phases: Vec<GcodePhase<'_>> = project
        .toolpath_configs()
        .iter()
        .enumerate()
        .filter_map(|(idx, tc)| {
            let result = project.get_result(idx)?;
            Some(GcodePhase {
                toolpath: result.toolpath(),
                spindle_rpm: effective_spindle_rpm(
                    &tc.operation,
                    project.post_config().spindle_speed,
                ),
                label: &tc.name,
                tool_number: None,
                coolant: CoolantMode::Off,
                pre_gcode: tc.pre_gcode.as_deref(),
                post_gcode: tc.post_gcode.as_deref(),
                controller_compensation: controller_comp_for_project_toolpath(tc),
            })
        })
        .collect();

    // Phase-level checked emit is currently a no-op enforcer (the project
    // gate above is the source of truth); pass policy through unchanged.
    export_gcode_phases_checked(&phases, post.as_ref(), sim_trace, policy)
}

/// Build a `ToolLoadReport` from a `ProjectSession`. Phase 1a wires
/// `chipload` (per-sample vs vendor LUT, requires `sim_trace`) and
/// `deflection` (geometric L/D, sample-independent). The `power`
/// criterion is stubbed `Unmodeled(NotImplemented)` until Phase 1b's
/// arc-engagement-driven power calc lands.
pub fn project_load_report(
    project: &ProjectSession,
    sim_trace: Option<&SimulationCutTrace>,
) -> crate::tool_load::ToolLoadReport {
    use crate::feeds::vendor_lut::{LutOperationFamily, LutPassRole};
    use crate::feeds::{OperationFamily, PassRole};

    let material = &project.stock_config().material;
    let mut per_toolpath = Vec::new();
    for tc in project.toolpath_configs() {
        if !tc.enabled {
            continue;
        }
        let Some(tool_cfg) = project.get_tool(crate::compute::tool_config::ToolId(tc.tool_id))
        else {
            // Toolpath references a tool that's been removed — skip rather
            // than fabricate a verdict.
            continue;
        };
        let tool_def = crate::compute::cutter::build_cutter(tool_cfg);
        let spec = tc.operation.spec();
        let lut_op = match spec.feeds_family {
            OperationFamily::Adaptive => LutOperationFamily::Adaptive,
            OperationFamily::Pocket => LutOperationFamily::Pocket,
            OperationFamily::Contour => LutOperationFamily::Contour,
            OperationFamily::Parallel => LutOperationFamily::Parallel,
            OperationFamily::Scallop => LutOperationFamily::Scallop,
            OperationFamily::Trace => LutOperationFamily::Trace,
            OperationFamily::Face => LutOperationFamily::Face,
        };
        let lut_pass = match spec.feeds_pass_role {
            PassRole::Roughing => LutPassRole::Roughing,
            PassRole::SemiFinish => LutPassRole::SemiFinish,
            PassRole::Finish => LutPassRole::Finish,
        };
        let machine = project.machine();
        // Item C: pass the operation's commanded feed rate so the
        // chipload guardrail can filter out transient (plunge/ramp/entry)
        // samples and only measure steady-state cutting against the LUT.
        let operation_feed_rate_mm_min = tc.operation.feed_rate();
        // gcode export keeps strict LUT/machine-ceiling behaviour — only
        // the optimizer routes wider tolerance bands. See `ToleranceBands`.
        let strict_tolerance = crate::tool_load::ToleranceBands::default();
        per_toolpath.push(crate::tool_load::ToolpathLoadVerdict {
            toolpath_id: tc.id,
            chipload: crate::tool_load::chipload::evaluate(
                tc.id,
                &tool_def,
                material,
                sim_trace,
                lut_op,
                lut_pass,
                operation_feed_rate_mm_min,
                tc.operation.op_type(),
                &strict_tolerance,
            ),
            power: crate::tool_load::power::evaluate(
                tc.id,
                &tool_def,
                material,
                machine,
                sim_trace,
                &strict_tolerance,
            ),
            deflection: crate::tool_load::deflection::evaluate(
                tc.id,
                &tool_def,
                material,
                sim_trace,
                &strict_tolerance,
            ),
        });
    }
    crate::tool_load::ToolLoadReport { per_toolpath }
}

/// Enforce a `ToolLoadExportPolicy` against a report. Returns a structured
/// error message naming each offending toolpath and criterion so the user
/// (or UI) sees exactly what to override.
pub fn enforce_load_policy(
    report: &crate::tool_load::ToolLoadReport,
    policy: &ToolLoadExportPolicy,
) -> Result<(), ExportError> {
    if !policy.accept_exceeded {
        let exceeded = report.exceeded_criteria();
        if !exceeded.is_empty() {
            let mut msg =
                String::from("G-code export refused: tool load exceeded on toolpath(s):\n");
            for (id, crits) in &exceeded {
                let reason_list: Vec<String> = crits
                    .iter()
                    .map(|ec| format!("{}={}", ec.label, ec.reason_label))
                    .collect();
                let _ = writeln!(msg, "  toolpath {id}: {}", reason_list.join(", "));
            }
            msg.push_str(
                "Pass `accept_exceeded=true` to override (this is a known-dangerous override).",
            );
            return Err(ExportError::new(msg));
        }
    }
    if !policy.accept_unmodeled && report.any_unmodeled() {
        let mut msg =
            String::from("G-code export refused: tool load not fully modeled for toolpath(s):\n");
        for v in &report.per_toolpath {
            if v.any_unmodeled() {
                let mut crits = Vec::new();
                if let crate::tool_load::ChiploadVerdict::Unmodeled { reason } = &v.chipload {
                    crits.push(format!("chipload={reason:?}"));
                }
                if let crate::tool_load::PowerVerdict::Unmodeled { reason } = &v.power {
                    crits.push(format!("power={reason:?}"));
                }
                if let crate::tool_load::DeflectionVerdict::Unmodeled { reason } = &v.deflection {
                    crits.push(format!("deflection={reason:?}"));
                }
                let _ = writeln!(msg, "  toolpath {}: {}", v.toolpath_id, crits.join(", "));
            }
        }
        msg.push_str("Pass `accept_unmodeled=true` to acknowledge unmodeled criteria.");
        return Err(ExportError::new(msg));
    }
    Ok(())
}

