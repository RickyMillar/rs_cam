/// Policy knobs reserved for tool-load-aware export checks.
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub struct ToolLoadExportPolicy {
    pub accept_unmodeled: bool,
    pub accept_exceeded: bool,
}

/// Error returned by checked G-code export.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ExportError {
    message: String,
}

impl ExportError {
    pub fn new(message: impl Into<String>) -> Self {
        Self {
            message: message.into(),
        }
    }
}

impl Display for ExportError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(&self.message)
    }
}

impl std::error::Error for ExportError {}

/// Direction for G41/G42 controller cutter compensation.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ControllerCompensation {
    /// G41 — cutter compensation left.
    Left,
    /// G42 — cutter compensation right.
    Right,
}

/// Coolant mode for G-code output.
#[derive(Clone, Copy, Debug, Default, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum CoolantMode {
    /// No coolant (default).
    #[default]
    Off,
    /// Mist coolant (M7).
    Mist,
    /// Flood coolant (M8).
    Flood,
    /// Both mist and flood (M7 + M8).
    Both,
}

impl CoolantMode {
    /// Emit the G-code command(s) to activate this coolant mode.
    /// Returns an empty string for `Off`.
    pub fn start_gcode(self) -> &'static str {
        match self {
            CoolantMode::Off => "",
            CoolantMode::Mist => "M7\n",
            CoolantMode::Flood => "M8\n",
            CoolantMode::Both => "M7\nM8\n",
        }
    }

    /// Returns true if coolant is active (not Off).
    pub fn is_active(self) -> bool {
        self != CoolantMode::Off
    }
}

/// Post-processor trait for machine-specific G-code dialects.
pub trait PostProcessor {
    fn name(&self) -> &str;
    fn preamble(&self, spindle_rpm: u32) -> String;
    fn postamble(&self) -> String;
    fn rapid(&self, x: f64, y: f64, z: f64) -> String;
    fn linear(&self, x: f64, y: f64, z: f64, feed: f64) -> String;
    fn arc_cw(&self, x: f64, y: f64, z: f64, i: f64, j: f64, feed: f64) -> String;
    fn arc_ccw(&self, x: f64, y: f64, z: f64, i: f64, j: f64, feed: f64) -> String;
    fn comment(&self, text: &str) -> String;
    fn decimal_places(&self) -> usize {
        3
    }

    /// Program pause for setup changes (M0). Override for machine-specific behavior.
    fn program_pause(&self, message: &str) -> String {
        let mut s = String::new();
        let _ = writeln!(s, "M5");
        s.push_str(&self.comment(message));
        let _ = writeln!(s, "M0");
        s
    }

    // Canned drilling cycles (default implementations)
    fn drill_simple(&self, x: f64, y: f64, z: f64, r: f64, feed: f64) -> String {
        format!("G81 X{x:.4} Y{y:.4} Z{z:.4} R{r:.4} F{feed:.1}\n")
    }
    fn drill_dwell(&self, x: f64, y: f64, z: f64, r: f64, feed: f64, dwell: f64) -> String {
        format!("G82 X{x:.4} Y{y:.4} Z{z:.4} R{r:.4} P{dwell:.3} F{feed:.1}\n")
    }
    fn drill_peck(&self, x: f64, y: f64, z: f64, r: f64, feed: f64, peck: f64) -> String {
        format!("G83 X{x:.4} Y{y:.4} Z{z:.4} R{r:.4} Q{peck:.4} F{feed:.1}\n")
    }
    fn drill_chip_break(&self, x: f64, y: f64, z: f64, r: f64, feed: f64, peck: f64) -> String {
        format!("G73 X{x:.4} Y{y:.4} Z{z:.4} R{r:.4} Q{peck:.4} F{feed:.1}\n")
    }
    fn drill_cancel(&self) -> String {
        "G80\n".to_owned()
    }
}

