/// A phase in a multi-operation job: toolpath + spindle speed + label.
pub struct GcodePhase<'a> {
    pub toolpath: &'a Toolpath,
    pub spindle_rpm: u32,
    pub label: &'a str,
    /// Raw G-code to emit before this toolpath's moves (user-defined).
    pub pre_gcode: Option<&'a str>,
    /// Raw G-code to emit after this toolpath's moves (user-defined).
    pub post_gcode: Option<&'a str>,
}

/// Emit G-code from multiple phases, inserting spindle speed changes between operations.
pub fn emit_gcode_phased(phases: &[GcodePhase<'_>], post: &dyn PostProcessor) -> String {
    if phases.is_empty() {
        return String::new();
    }

    let mut output = String::new();
    let first_rpm = phases[0].spindle_rpm;
    output.push_str(&post.preamble(first_rpm));

    let mut current_rpm = first_rpm;
    let mut last_feed: Option<f64> = None;

    for phase in phases {
        output.push_str(&post.comment(phase.label));

        if phase.spindle_rpm != current_rpm {
            let _ = writeln!(output, "M3 S{}", phase.spindle_rpm);
            current_rpm = phase.spindle_rpm;
        }

        if let Some(pre) = phase.pre_gcode
            && !pre.is_empty()
        {
            output.push_str(pre);
            if !pre.ends_with('\n') {
                output.push('\n');
            }
        }

        for m in &phase.toolpath.moves {
            match m.move_type {
                MoveType::Rapid => {
                    output.push_str(&post.rapid(m.target.x, m.target.y, m.target.z));
                    last_feed = None;
                }
                MoveType::Linear { feed_rate } => {
                    if last_feed != Some(feed_rate) {
                        output
                            .push_str(&post.linear(m.target.x, m.target.y, m.target.z, feed_rate));
                        last_feed = Some(feed_rate);
                    } else {
                        let dp = post.decimal_places();
                        let _ = writeln!(
                            output,
                            "G1 X{:.dp$} Y{:.dp$} Z{:.dp$}",
                            m.target.x, m.target.y, m.target.z
                        );
                    }
                }
                MoveType::ArcCW { i, j, feed_rate } => {
                    output.push_str(
                        &post.arc_cw(m.target.x, m.target.y, m.target.z, i, j, feed_rate),
                    );
                    last_feed = Some(feed_rate);
                }
                MoveType::ArcCCW { i, j, feed_rate } => {
                    output.push_str(
                        &post.arc_ccw(m.target.x, m.target.y, m.target.z, i, j, feed_rate),
                    );
                    last_feed = Some(feed_rate);
                }
            }
        }

        if let Some(post_gc) = phase.post_gcode
            && !post_gc.is_empty()
        {
            output.push_str(post_gc);
            if !post_gc.ends_with('\n') {
                output.push('\n');
            }
        }
    }

    output.push_str(&post.postamble());
    output
}

/// A group of toolpath phases belonging to one setup.
pub struct GcodeSetupPhase<'a> {
    pub setup_label: &'a str,
    pub phases: Vec<GcodePhase<'a>>,
}

/// Emit G-code for multiple setups with M0 pauses between them.
pub fn emit_gcode_multi_setup(
    setups: &[GcodeSetupPhase<'_>],
    post: &dyn PostProcessor,
    safe_z: f64,
) -> String {
    if setups.is_empty() {
        return String::new();
    }

    let mut output = String::new();
    let first_rpm = setups
        .iter()
        .flat_map(|setup| setup.phases.iter())
        .map(|phase| phase.spindle_rpm)
        .next()
        .unwrap_or(18_000);

    output.push_str(&post.preamble(first_rpm));

    let mut current_rpm = first_rpm;
    let mut last_feed: Option<f64> = None;
    let dp = post.decimal_places();

    for (setup_index, setup) in setups.iter().enumerate() {
        if setup_index > 0 {
            let _ = writeln!(output, "G0 Z{safe_z:.dp$}");
            output.push_str(&post.program_pause(&format!("Setup change: {}", setup.setup_label)));

            let next_rpm = setup
                .phases
                .first()
                .map(|phase| phase.spindle_rpm)
                .unwrap_or(current_rpm);
            let _ = writeln!(output, "M3 S{next_rpm}");
            current_rpm = next_rpm;
            last_feed = None;
        }

        output.push_str(&post.comment(&format!("=== {} ===", setup.setup_label)));

        for phase in &setup.phases {
            output.push_str(&post.comment(phase.label));

            if phase.spindle_rpm != current_rpm {
                let _ = writeln!(output, "M3 S{}", phase.spindle_rpm);
                current_rpm = phase.spindle_rpm;
            }

            if let Some(pre) = phase.pre_gcode
                && !pre.is_empty()
            {
                output.push_str(pre);
                if !pre.ends_with('\n') {
                    output.push('\n');
                }
            }

            for m in &phase.toolpath.moves {
                match m.move_type {
                    MoveType::Rapid => {
                        output.push_str(&post.rapid(m.target.x, m.target.y, m.target.z));
                        last_feed = None;
                    }
                    MoveType::Linear { feed_rate } => {
                        if last_feed != Some(feed_rate) {
                            output.push_str(
                                &post.linear(m.target.x, m.target.y, m.target.z, feed_rate),
                            );
                            last_feed = Some(feed_rate);
                        } else {
                            let _ = writeln!(
                                output,
                                "G1 X{:.dp$} Y{:.dp$} Z{:.dp$}",
                                m.target.x, m.target.y, m.target.z
                            );
                        }
                    }
                    MoveType::ArcCW { i, j, feed_rate } => {
                        output.push_str(
                            &post.arc_cw(m.target.x, m.target.y, m.target.z, i, j, feed_rate),
                        );
                        last_feed = Some(feed_rate);
                    }
                    MoveType::ArcCCW { i, j, feed_rate } => {
                        output.push_str(
                            &post.arc_ccw(m.target.x, m.target.y, m.target.z, i, j, feed_rate),
                        );
                        last_feed = Some(feed_rate);
                    }
                }
            }

            if let Some(post_gc) = phase.post_gcode
                && !post_gc.is_empty()
            {
                output.push_str(post_gc);
                if !post_gc.ends_with('\n') {
                    output.push('\n');
                }
            }
        }
    }

    output.push_str(&post.postamble());
    output
}

/// Replace G0 rapid moves with G1 at a high feedrate.
/// Used for machines with unpredictable rapid behavior (e.g., GRBL "dogleg" rapids).
pub fn replace_rapids_with_feed(gcode: &str, high_feedrate: f64) -> String {
    let mut output = String::with_capacity(gcode.len());
    for line in gcode.lines() {
        let trimmed = line.trim();
        if trimmed.starts_with("G0 ") || trimmed.starts_with("G0X") {
            // Replace G0 with G1 and append feedrate
            let rest = &trimmed[2..];
            output.push_str(&format!("G1{rest} F{high_feedrate:.1}\n"));
        } else {
            output.push_str(line);
            output.push('\n');
        }
    }
    output
}

/// Get a post-processor by name.
pub fn get_post_processor(name: &str) -> Option<Box<dyn PostProcessor>> {
    match name.to_lowercase().as_str() {
        "grbl" => Some(Box::new(GrblPost)),
        "linuxcnc" => Some(Box::new(LinuxCncPost)),
        "mach3" => Some(Box::new(Mach3Post)),
        _ => None,
    }
}

#[cfg(test)]
