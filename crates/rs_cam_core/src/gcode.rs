//! G-code generation and post-processors.
//!
//! Note: `let _ = write!(...)` is used throughout this module for writing to `String`.
//! Writing to `String` is infallible (only fails on OOM, which panics regardless),
//! so discarding the `Result` with `let _ =` is safe.

use crate::toolpath::{MoveType, Toolpath};
use std::fmt::Write;

/// Post-processor trait for machine-specific G-code dialects.
pub trait PostProcessor {
    fn name(&self) -> &str;
    fn preamble(&self, spindle_rpm: u32) -> String;
    fn postamble(&self) -> String;
    fn rapid(&self, x: f64, y: f64, z: f64) -> String;
    fn linear(&self, x: f64, y: f64, z: f64, feed: f64) -> String;
    fn arc_cw(&self, x: f64, y: f64, z: f64, i: f64, j: f64, feed: f64) -> String;
    fn arc_ccw(&self, x: f64, y: f64, z: f64, i: f64, j: f64, feed: f64) -> String;
    fn comment(&self, text: &str) -> String;
    fn decimal_places(&self) -> usize { 3 }
}

/// GRBL-compatible post-processor.
pub struct GrblPost;

impl PostProcessor for GrblPost {
    fn name(&self) -> &str { "GRBL" }

    fn preamble(&self, spindle_rpm: u32) -> String {
        let mut s = String::new();
        let _ = writeln!(s, "(Generated by rs_cam)");
        let _ = writeln!(s, "G17 G21 G90 G40 G49 G80");
        let _ = writeln!(s, "M3 S{spindle_rpm}");
        s
    }

    fn postamble(&self) -> String {
        let mut s = String::new();
        let _ = writeln!(s, "M5");
        let _ = writeln!(s, "G0 Z10.000");
        let _ = writeln!(s, "M30");
        s
    }

    fn rapid(&self, x: f64, y: f64, z: f64) -> String {
        format!("G0 X{x:.3} Y{y:.3} Z{z:.3}\n")
    }

    fn linear(&self, x: f64, y: f64, z: f64, feed: f64) -> String {
        format!("G1 X{x:.3} Y{y:.3} Z{z:.3} F{feed:.0}\n")
    }

    fn arc_cw(&self, x: f64, y: f64, z: f64, i: f64, j: f64, feed: f64) -> String {
        format!("G2 X{x:.3} Y{y:.3} Z{z:.3} I{i:.3} J{j:.3} F{feed:.0}\n")
    }

    fn arc_ccw(&self, x: f64, y: f64, z: f64, i: f64, j: f64, feed: f64) -> String {
        format!("G3 X{x:.3} Y{y:.3} Z{z:.3} I{i:.3} J{j:.3} F{feed:.0}\n")
    }

    fn comment(&self, text: &str) -> String {
        format!("({text})\n")
    }
}

